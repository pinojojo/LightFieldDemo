	void ImGuiLayer::ConfigFonts()
	{

		// 中英文字体
		ImGuiIO& io = ImGui::GetIO();
		FILE* inFile = fopen("assets/fonts/char_table.txt", "rb");
		unsigned char* charBuf;
		fseek(inFile, 0, SEEK_END);
		int fileLen = ftell(inFile);
		charBuf = new unsigned char[fileLen];
		fseek(inFile, 0, SEEK_SET);
		fread(charBuf, fileLen, 1, inFile);
		fclose(inFile);
		static ImVector<ImWchar> myRange;
		ImFontGlyphRangesBuilder myGlyph;
		myGlyph.AddText((const char*)charBuf);
		myGlyph.BuildRanges(&myRange);
		delete[] charBuf;

		ImFontConfig config;
		config.OversampleH = 1;
		config.OversampleV = 1;
		config.GlyphExtraSpacing.x = 0.5f;
		ImGuiStyleConfig::GetInstance().BodyFont = io.Fonts->AddFontFromFileTTF("assets/fonts/NotoSansSC-Regular.otf", 20.0f, &config, myRange.Data);

		// 谷歌图标
		static const ImWchar icons_ranges[] = { ICON_MIN_MD, ICON_MAX_MD, 0 };
		ImFontConfig icons_config; icons_config.MergeMode = true; icons_config.PixelSnapH = true;
		io.Fonts->AddFontFromFileTTF("assets/fonts/MaterialIcons-Regular.ttf", 18.0f, &icons_config, icons_ranges);
		io.Fonts->Build();

		// 标题字体
		icons_config.MergeMode = false;
		ImGuiStyleConfig::GetInstance().TitleFont = io.Fonts->AddFontFromFileTTF("assets/fonts/NotoSansSC-Bold.otf", 25.0f, &config, myRange.Data);


		// 
		ImGuiStyleConfig::GetInstance().ThinItalicFont = io.Fonts->AddFontFromFileTTF("assets/fonts/NotoSansSC-Light.otf", 18.0f, &config, myRange.Data);


	}

